/** Toán tử gán 
 
 Toán tử     Ví dụ     Tương đương
 =           x=y       x = y
 +=          x+=y      x = x + y
 -=          x-=y      x = x - y
 *=          x*=y      x = x * y 
 /=          x/=y      x = x / y 
 **=         x**=y     x = x ** y

 */

 var a = 3;
a += 4;  // tương đương a = a + 4
console.log(a);

var b = 5;
b -= 4; // tương đương b = b - 5
console.log(b);

var c = 9;
c *= 2; // tương đương c = c * 2
console.log(c);

var d = 10;
d /= 2; // tương đương d = d / 2;
console.log(d);

var e = 2;
e **= 2;// tương đương e = e ** 2;
console.log(e);

